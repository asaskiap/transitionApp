const Britt_EN = (props) => {
  return (
    <Article>
      <Paragraph>
        Some careers start with obstacles and then pick up speed. Britt Folk's
        path began in her home town of Bernau near Berlin with an ambitious
        ballet teacher at the Kreiskulturhaus. Twice a week, little Britt went
        to class there from the second grade on, heard the praise for the instep
        of a classmate, and actually thought her instep was good, too. In the
        pioneer newspaper "Trommel" she saw an advertisement for the Palucca
        School in Dresden, sent off the application, traveled on the Vindobona
        Express, "a great experience" for her, to the aptitude test. Everything
        went well until her "angel wings," protruding shoulder blades, were
        discovered. Undone, she went back on the Vindobona.
      </Paragraph>
      <Paragraph>
        A few weeks later, the "Trommel" now advertised the Berlin State Ballet
        School. In a multi-stage test, Britt prevailed in the rush of
        applicants, received a six-month probationary period, with the
        requirement for anti-"angel wing" training with the school's
        physiotherapist. Beaming, but also saddened by the separation from her
        parents, she moved into the boarding school in 1976 "with my plaid air
        suitcase." Every day, she walked past the photos of the school's star
        graduates - Steffi Scherzer, Jutta Deutschland, Angela Philipp - which
        spurred her on. Soon, Britt's talent was also noticed. Pedagogue Stefan
        Lux used her as a soloist in his school choreographies: "What have they
        done with the rain?" around atomic-polluted rain, "Children's Crusade"
        to music by Benjamin Britten and after the poem "Children's Crusade
        1939" by Bertolt Brecht, "Concerto for the Youth" as a symphonic
        transposition of the 2nd Piano Concerto by Dmitri Shostakovich.
        Admission to the remedial class, preparation for national and
        international competitions, such as Moscow and Varna, were the logical
        consequence.
      </Paragraph>
      <Paragraph>
        After seven years of training, Britt went to the Komische Oper in 1983
        to Tom Schilling, where she immediately studied solo parts, "Romeo and
        Juliet", "Swan Lake", "Elective Affinities", "A New Midsummer Night's
        Dream", Schilling's showpieces. Birgit Scherzer, then still an ensemble
        dancer, also choreographed for her, for example the "Solo with Tailcoat
        and Hat". After three years, Britt nevertheless moved to the Staatsoper
        Unter den Linden, "I wanted to realize my childish ballerina dream of
        the great classical music". She was particularly enthusiastic about the
        works of George Balanchine there, including Coppelia in Tom Schilling's
        guest production. Then the problems with her back, scoliosis, began. In
        addition, there was the insecurity in this time of upheaval and change:
        the "dancer's pension" of the GDR, which had once been fought for with
        great difficulty, was cancelled in the new Germany. The motto was to
        reorient herself, to become a teacher at the State Ballet School or to
        study medicine, for which she had a place, as well as for the subject of
        social and economic communication at the Hochschule der Künste. She had
        already obtained her high school diploma at night school. Believing in
        orderly circumstances, she then trained as a physiotherapist for three
        years in a motley crew at the Charité. Now in her second profession
        since 1996, she feels completely secure, working in a small private
        practice after several stops in between. "There," she says, "is not such
        a mass flow, everything runs in personal contact with the patients." In
        the meantime, she has also studied "spiraldynamics," a movement and
        therapy concept from Switzerland, and implements its findings in her
        day-to-day work. And almost incidentally, she has found her way back to
        dance: For the past year, she has been teaching children's dance twice a
        week at a private school. The shy girl of yesteryear has become a
        self-confident woman with an infectiously cheerful laugh, a mother of
        two with an intact family. Which proves: there is also a fulfilling life
        after dance.{' '}
      </Paragraph>
      <ArticleBy>Volkmar Draeger</ArticleBy>
    </Article>
  );
};

